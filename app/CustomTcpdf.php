/**
 * Creates an example PDF TEST document using TCPDF
 * @package com.tecnick.tcpdf
 * @abstract TCPDF - Example: Custom Header and Footer
 * @author Nicola Asuni
 * @since 2008-03-04
 */

// Include the main TCPDF library (search for installation path).
namespace App;

use Elibyy\TCPDF\Facades\TCPDF;

// Extend the TCPDF class to create custom Header and Footer
class CustomTcpdf extends TCPDF {

    //Page header

    public function Header() {
        // Logo
        $src=storage_path('uploads/company_assets/');
        define('K_PATH_IMAGES', 'uploads/company_assets/default/');
        $image_file = K_PATH_IMAGES.'default_!!header.gif';
        $this->Image($image_file, 10, 10, 15, '', 'JPG', '', 'T', false, 300, '', false, false, 0, false, false, false);
        // Set font
        $this->SetFont('helvetica', 'B', 20);
        // Title
        $this->Cell(0, 15, '<< TCPDF Example 003 >>', 0, false, 'C', 0, '', 0, false, 'M', 'M');
    }

    // Page footer
    public function Footer() {
        // Logo
        $src=storage_path().'uploads/company_assets/default_!!header.gif';

        $this->Image($src, 10, 10, 15, '', 'JPG', '', 'T', false, 300, '', false, false, 0, false, false, false);
        // Position at 15 mm from bottom
        $this->SetY(-15);
        // Set font
        $this->SetFont('helvetica', 'I', 8);
        // Page number
        $this->Cell(0, 10, 'Page '.$this->getAliasNumPage().'/'.$this->getAliasNbPages(), 0, false, 'C', 0, '', 0, false, 'T', 'M');
    }
}

?>